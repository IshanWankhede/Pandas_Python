# • 	Data Structures:
# • 	"Series" → 1D labeled array (like a column in Excel).
# • 	"DataFrame" → 2D labeled data structure (like a full table with rows and columns).

# • 	Data Handling:
# • 	Load data from CSV, Excel, SQL, JSON, etc.
# • 	Clean messy datasets (handle missing values, duplicates).
# • 	Transform and reshape data easily.
# • 	Integration:
# • 	Works seamlessly with NumPy (numerical ops), Matplotlib/Seaborn 
# (visualization), SciPy (statistics), and Scikit-learn (machine learning).

